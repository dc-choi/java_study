package org.yama.model;

public class Twins {
    private int sunsu_id;
    private String sunsu;
    private int salary;

    public int getSunsu_id() {
        return sunsu_id;
    }

    public void setSunsu_id(int sunsu_id) {
        this.sunsu_id = sunsu_id;
    }

    public String getSunsu() {
        return sunsu;
    }

    public void setSunsu(String sunsu) {
        this.sunsu = sunsu;
    }

    public int getSalary() {
        return salary;
    }

    public void setSalary(int salary) {
        this.salary = salary;
    }
}
